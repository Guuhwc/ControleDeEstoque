    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            //BOTAO CADASTRAR
            
            Estoque p = new Estoque(); //Instancia o objeto "p" da classe de gets e sets
            CRUD p2 = new CRUD(); //Instancia o objeto "p2" da classe de funções mysql
            
            //id, nomeBebida, lote, qtd, validade, tipo, recebimento, preco
            
            //Joga os dados das jTextField pros metodos set
            p.setNomeBebida(txtNomeBebida.getText());
            p.setLote(txtLote.getText());
            p.setQtd(Integer.parseInt(txtQtd.getText()));
            p.setValidade(txtValidade.getText());
            p.setTipo(txtTipo.getText());
            p.setRecebimento(txtRecebimento.getText());
            p.setPreco(Double.parseDouble(txtPreco.getText()));
            //
            p2.salvarInsert(p); //Chama o metodo da classe de funções para inserir os dados no banco de dados 
            
            //Limpa as caixas de texto
            txtNomeBebida.setText("");
            txtLote.setText("");
            txtQtd.setText("");
            txtValidade.setText("");
            txtTipo.setText("");
            txtRecebimento.setText("");
            txtPreco.setText("");
            //
            this.lerJTable(); //Exibe os dados no jTable
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao inserir os dados: " + ex);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //BOTAO EXCLUIR
        //Confirmação se deseja realmente excluir
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja excluir o item selecionado (ID: "+(int) jtblFuncaoSelect.getValueAt(jtblFuncaoSelect.getSelectedRow(), 0)+")?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (opcao == JOptionPane.YES_OPTION)
        {
        //
            if (this.jtblFuncaoSelect.getSelectedRow() != -1) //Se o indice da linha clicada for diferente de -1:
            {
                Estoque p = new Estoque();
                CRUD p2 = new CRUD();

                p.setId((int) jtblFuncaoSelect.getValueAt(jtblFuncaoSelect.getSelectedRow(), 0));

                try {
                    p2.deletarDelete(p);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro: " + ex);
                }

                txtNomeBebida.setText("");
                txtLote.setText("");
                txtQtd.setText("");
                txtValidade.setText("");
                txtTipo.setText("");
                txtRecebimento.setText("");
                txtPreco.setText("");

                try {
                    this.lerJTable();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro: " + ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum item selecionado.");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

