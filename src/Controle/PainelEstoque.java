    public void lerJTable() throws SQLException 
    {
        //METODO PARA LER O JTABLE
        DefaultTableModel modelo = (DefaultTableModel) this.jtblFuncaoSelect.getModel(); //Define o modelo do jTable
        modelo.setNumRows(0); //Define o número de linhas padrao

        CRUD p2 = new CRUD(); //Instancia o objeto "p2" da classe de funções mysql

        for (Estoque p : p2.lerSelect()) //função forEach
        {
            //Pega os dados e joga no jTable
            modelo.addRow(new Object[]{
                //id, nomeBebida, lote, qtd, validade, tipo, recebimento, preco
                p.getId(),
                p.getNomeBebida(),
                p.getLote(),
                p.getQtd(),
                p.getValidade(),
                p.getTipo(),
                p.getRecebimento(),
                p.getPreco(),
                p.getPrecoTotal()
            });
            //
        }
    }
    
